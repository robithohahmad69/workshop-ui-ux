// ============================================================
// HELPER: Tentukan productImageKey dari teks judul
// ============================================================
function resolveProductKey(titleText) {
    const t = titleText.toLowerCase();
    if (t.includes('sangkuriang'))                    return 'benihLeleSangkuriang';
    if (t.includes('phyton'))                         return 'benihLelePhyton';
    if (t.includes('mesir'))                          return 'benihLeleMesir';
    if (t.includes('781-2') || t.includes('pelet 781')) return 'pakanPelet781';
    if (t.includes('grower'))                         return 'pakanPeletGrower';
    if (t.includes('starter'))                        return 'pakanPeletStarter';
    if (t.includes('cincing'))                        return 'pakanCincing';
    if (t.includes('vitamin') || t.includes('grow')) return 'vitaminIkanGrow';
    if (t.includes('booster') || t.includes('imun')) return 'boosterImun';
    if (t.includes('amankan') || t.includes('obat organik')) return 'obatOrganikAmankan';
    if (t.includes('probiotik') || t.includes('bakteri')) return 'probiotikBakteri';
    if (t.includes('garam') || t.includes('krosok')) return 'garamKrosok';
    return 'benihLeleSangkuriang'; // fallback
}

// ============================================================
// HELPER: Tentukan articleImageKey dari teks judul
// ============================================================
function resolveArticleKey(titleText) {
    const t = titleText.toLowerCase();
    if (t.includes('kualitas air') || t.includes('air kolam')) return 'kualitasAir';
    if (t.includes('jadwal') || t.includes('pakan'))           return 'jadwalPakan';
    if (t.includes('analisis') || t.includes('usaha') || t.includes('bisnis')) return 'analisisUsaha';
    if (t.includes('bintik putih'))                            return 'bintikPutih';
    if (t.includes('terpal'))                                  return 'kolamTerpal';
    if (t.includes('maggot'))                                  return 'budidayaMaggot';
    if (t.includes('pemasaran'))                               return 'pemasaran';
    if (t.includes('hujan'))                                   return 'musimHujan';
    if (t.includes('stress'))                                  return 'stressIkan';
    if (t.includes('profit') || t.includes('keuntungan'))      return 'profitSiklus';
    if (t.includes('hemat'))                                   return 'hematPakan';
    if (t.includes('pemijahan'))                               return 'pemijahan';
    return 'kualitasAir'; // fallback
}

