// DETAIL PRODUK — baca localStorage untuk tahu produk mana
// ============================================================
function setupDetailProductPage() {
    const savedProduct = localStorage.getItem('selectedProduct') || '';
    const key = resolveProductKey(savedProduct);
    const productAlt = savedProduct || 'Produk';

    // Main image
    const mainImage = document.querySelector('.main-image');
    if (mainImage) {
        mainImage.querySelector('i')?.remove();
        mainImage.querySelector('img')?.remove();

        const img = document.createElement('img');
        img.src = IMAGES.products[key];
        img.alt = productAlt;
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
        mainImage.appendChild(img);
    }

    // Thumbnails — thumbnail pertama ikut gambar produk yang dipilih
    const thumbnailImages = [
        IMAGES.products[key],
        IMAGES.productThumbnails.thumbnail2,
        IMAGES.productThumbnails.thumbnail3,
        IMAGES.productThumbnails.thumbnail4,
    ];
    document.querySelectorAll('.thumbnail').forEach((thumb, index) => {
        thumb.querySelector('i')?.remove();
        thumb.querySelector('img')?.remove();

        const img = document.createElement('img');
        img.src = thumbnailImages[index] || thumbnailImages[0];
        img.alt = `Thumbnail ${index + 1}`;
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
        thumb.appendChild(img);
    });

    // Related products
    const relatedPool = [
        IMAGES.products.pakanPelet781,
        IMAGES.products.vitaminIkanGrow,
        IMAGES.products.benihLelePhyton,
        IMAGES.products.probiotikBakteri,
    ];
    document.querySelectorAll('.related-image').forEach((parent) => {
        parent.querySelector('i')?.remove();
        parent.querySelector('img')?.remove();

        const img = document.createElement('img');
        img.src = relatedPool[Math.floor(Math.random() * relatedPool.length)];
        img.alt = 'Produk Terkait';
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
        parent.appendChild(img);
    });

    // Render data produk dari produk-data.js
    if (typeof renderDetailProduk === 'function') {
        renderDetailProduk();
    }
}

