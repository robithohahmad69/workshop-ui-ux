// Fungsi untuk mendapatkan URL gambar berdasarkan kategori dan ID
// ============================================================
function getImageUrl(category, imageId) {
    if (IMAGES[category] && IMAGES[category][imageId]) {
        return IMAGES[category][imageId];
    }
    return 'https://images.unsplash.com/photo-1525498128493-380d1990a112?w=600&h=600&fit=crop';
}

// ============================================================
// INJECT IMAGES — dipanggil di semua halaman (index, katalog, artikel, keranjang, dll)
// ============================================================
function injectImages() {

    // --- Hero Background ---
    const heroSection = document.querySelector('.hero');
    if (heroSection && IMAGES.hero.background) {
        heroSection.style.backgroundImage = `linear-gradient(rgba(27, 67, 50, 0.85), rgba(27, 67, 50, 0.85)), url('${IMAGES.hero.background}')`;
        heroSection.style.backgroundSize = 'cover';
        heroSection.style.backgroundPosition = 'center';
    }

    // --- Product Cards ---
    document.querySelectorAll('.product-card').forEach((card) => {
        const productImage = card.querySelector('.product-image');
        const productTitle = card.querySelector('h3, .product-name');
        if (!productImage || !productTitle) return;

        const key = resolveProductKey(productTitle.textContent);

        const icon = productImage.querySelector('i');
        const existingImg = productImage.querySelector('img');
        if (icon) icon.remove();
        if (existingImg) existingImg.remove();

        const img = document.createElement('img');
        img.src = IMAGES.products[key];
        img.alt = productTitle.textContent.trim();
        productImage.appendChild(img);
    });

    // --- Article Cards ---
    document.querySelectorAll('.article-card').forEach((articleCard) => {
        const articleImage = articleCard.querySelector('.article-image');
        const articleTitle = articleCard.querySelector('h3');
        if (!articleImage || !articleTitle) return;

        const key = resolveArticleKey(articleTitle.textContent);

        const icon = articleImage.querySelector('i');
        const existingImg = articleImage.querySelector('img');
        if (icon) icon.remove();
        if (existingImg) existingImg.remove();

        const img = document.createElement('img');
        img.src = IMAGES.articles[key];
        img.alt = articleTitle.textContent.trim();
        articleImage.appendChild(img);
    });

    // --- Cart & Order Item Images ---
    document.querySelectorAll('.item-image, .order-item-image').forEach((itemImage) => {
        const itemName = itemImage.closest('.cart-item, .order-products')
            ?.querySelector('.item-name, .order-product-item span')
            ?.textContent.toLowerCase() || '';

        const icon = itemImage.querySelector('i');
        const existingImg = itemImage.querySelector('img');
        if (icon) icon.remove();
        if (existingImg) existingImg.remove();

        let key = 'benihLeleSangkuriang';
        if (itemName.includes('pakan') || itemName.includes('pelet')) key = 'pakanPelet781';
        else if (itemName.includes('vitamin')) key = 'vitaminIkanGrow';

        const img = document.createElement('img');
        img.src = IMAGES.products[key];
        img.alt = 'Produk';
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
        itemImage.appendChild(img);
    });

    // --- Fallback: icon yang belum tercover di product-image ---
    document.querySelectorAll('.product-image i').forEach((icon) => {
        const parent = icon.closest('.product-image');
        if (parent && !parent.querySelector('img')) {
            icon.remove();
            const img = document.createElement('img');
            img.src = IMAGES.products.benihLeleSangkuriang;
            img.alt = 'Produk';
            img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
            parent.appendChild(img);
        }
    });
}

