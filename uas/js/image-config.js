// DETAIL ARTIKEL — baca localStorage untuk tahu artikel mana
// ============================================================
function setupDetailArticlePage() {
    const savedArticle = localStorage.getItem('selectedArticle') || '';
    const key = resolveArticleKey(savedArticle);
    const articleAlt = savedArticle || 'Artikel';

    // Hero image artikel — ukuran tetap dari CSS, hanya gambarnya yang diganti
    const heroImage = document.querySelector('.article-hero-image');
    if (heroImage) {
        heroImage.querySelector('i')?.remove();
        heroImage.querySelector('img')?.remove();

        const img = document.createElement('img');
        img.src = IMAGES.articles[key];
        img.alt = articleAlt;
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover;';
        heroImage.appendChild(img);
    }

    // Load artikel content dari ARTIKEL_DATA
    const artikel = ARTIKEL_DATA[savedArticle];
    if (artikel) {
        // Update title
        const titleEl = document.querySelector('.article-title');
        if (titleEl) titleEl.textContent = artikel.judul;

        // Update kategori badge
        const tagEl = document.querySelector('.article-tag');
        if (tagEl) {
            const icon = tagEl.querySelector('i');
            tagEl.innerHTML = '';
            if (icon) tagEl.appendChild(icon);
            tagEl.appendChild(document.createTextNode(' ' + artikel.kategori));
        }

        // Update meta
        const dateEl = document.querySelector('.article-meta-detail span:first-child');
        if (dateEl && !dateEl.querySelector('.fas')) {
            dateEl.innerHTML = `<i class="fas fa-calendar"></i> ${artikel.tanggal}`;
        }

        // Update views dan komentar
        const metaSpans = document.querySelectorAll('.article-meta-detail span');
        if (metaSpans.length >= 4) {
            if (!metaSpans[2].querySelector('.fas')) {
                metaSpans[2].innerHTML = `<i class="fas fa-eye"></i> ${artikel.views} views`;
            }
            if (!metaSpans[3].querySelector('.fas')) {
                metaSpans[3].innerHTML = `<i class="fas fa-comment"></i> ${artikel.komentar} komentar`;
            }
        }

        // Update article body
        const bodyEl = document.querySelector('.article-body');
        if (bodyEl) {
            bodyEl.innerHTML = `<p>${artikel.deskripsiLengkap}</p>`;
        }

        // Update penulis
        const authorNameEl = document.querySelector('.author-info h4');
        if (authorNameEl) authorNameEl.textContent = artikel.penulis;

        const authorBioEl = document.querySelector('.author-info p');
        if (authorBioEl) authorBioEl.textContent = artikel.penulisBio;

        // Update tags
        const tagsEl = document.querySelector('.article-tags');
        if (tagsEl) {
            tagsEl.innerHTML = '';
            artikel.tags.forEach(tag => {
                const tagLink = document.createElement('a');
                tagLink.href = '#';
                tagLink.className = 'tag';
                tagLink.textContent = tag;
                tagsEl.appendChild(tagLink);
            });
        }

        // Update page title
        document.title = `${artikel.judul} - BABA FARM`;
    }

    // Related articles — gambar sesuai dengan judul artikel
    document.querySelectorAll('.related-card').forEach((card) => {
        const relatedImage = card.querySelector('.related-image');
        const relatedTitle = card.querySelector('h4');
        if (!relatedImage || !relatedTitle) return;

        const key = resolveArticleKey(relatedTitle.textContent);

        relatedImage.querySelector('i')?.remove();
        relatedImage.querySelector('img')?.remove();

        const img = document.createElement('img');
        img.src = IMAGES.articles[key];
        img.alt = relatedTitle.textContent.trim();
        img.style.cssText = 'width: 100%; height: 100%; object-fit: cover; border-radius: inherit;';
        relatedImage.appendChild(img);
    });
}

// ============================================================
// INIT — jalankan saat DOM siap
// ============================================================
function init() {
    injectImages();

    const path = window.location.pathname;
    if (path.includes('detail-produk')) setupDetailProductPage();
    if (path.includes('detail-artikel')) setupDetailArticlePage();
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
} else {
    init();
}